    HW_WR_REG8((uint32_t)dec_5b6b + 0x30, 0x1c);
    HW_WR_REG8((uint32_t)dec_5b6b + 0x31, 0x03);
    HW_WR_REG8((uint32_t)dec_5b6b + 0x32, 0x13);
    HW_WR_REG8((uint32_t)dec_5b6b + 0x33, 0x18);
    HW_WR_REG8((uint32_t)dec_5b6b + 0x34, 0x0b);
