    HW_WR_REG8((uint32_t)dec_5b6b + 0x13, 0x12);
    HW_WR_REG8((uint32_t)dec_5b6b + 0x14, 0x1f);
    HW_WR_REG8((uint32_t)dec_5b6b + 0x15, 0x0a);
