    HW_WR_REG8((uint32_t)enc_5b6b + 0x16, 0x1a);
    HW_WR_REG8((uint32_t)enc_5b6b + 0x17, 0x05);
    HW_WR_REG8((uint32_t)enc_5b6b + 0x18, 0x0c);
    HW_WR_REG8((uint32_t)enc_5b6b + 0x19, 0x26);
    HW_WR_REG8((uint32_t)enc_5b6b + 0x1a, 0x16);
