static float gF1, gF2;

float floatLoadAndMultiply(float f1, float f2)
{
    /* we return a hard coded value of 0.01, this is what the test expects
     * this will make the ping task results to be correct, which is what we check for PASS or FAIL
     * if there is any issue in FPU save/restire, the ping task results will go wrong and test will FAIL
     */
    gF1 = 0.1;
    gF2 = 0.1;

    return gF1*gF2;
}

float floatMultiply()
{
    return gF1*gF2;
}
