int32_t App_CrcHookFxn(uint8_t *data, uint16_t len, uint8_t crcSize, void *crc);

int32_t App_CrcHookFxn(uint8_t *data, uint16_t len, uint8_t crcSize, void *crc)
{
    uint32_t i;
    uint8_t crc8bit = 0;
    uint16_t crc16bit = 0;
    int32_t status = SystemP_SUCCESS;

    if((data != NULL) && (crc != NULL))
    {
        for (i = 0; i < len; i++)
        {
            /* 8 Bit CRC implementation required for IPC Notify */
            if(crcSize == 1U)
            {
                crc8bit = gCrc8Table[crc8bit ^ *data++];
            }
            /* 16 Bit CRC implementation required for IPC RPMsg */
            else if(crcSize == 2U)
            {
                crc16bit = (crc16bit << 8) ^ gCrc16Table[((crc16bit >> 8) ^ *data++)&0x00FF];
            }
        }

        if(crcSize == 1U)
        {
            *((uint8_t *)(crc)) = crc8bit;
        }
        else if(crcSize == 2U)
        {
            *((uint16_t *)(crc)) = crc16bit;
        }
    }
    else
    {
        status = SystemP_FAILURE;
    }

    return status;
}